# Intro
print('Um programa que receba 3 números e calcule a média aritmética dos mesmos:')
num_1 = float(input('Digite o primeiro: '))
num_2 = float(input('Digite aqui o segundo: '))
num_3 = float(input('Digite aqui o terceiro: '))

# Meio
media_simples = (num_1 + num_2 + num_3) / 3

# Saída
print('A média simples é: ', media_simples)