# Entrada
print('Leia dois números inteiros e mostre sua divisão: ')
num_1 = float(input('Digite aqui o primeiro número: '))
num_2 = float(input('Digite aqui o segundo número: '))

# Processamento
quociente = num_1 // num_2
resto_1 = num_1 % num_2

# Fim
print('O quociente entre os dois números é: ', quociente)
print('O resto da divisão entre os dois números é: ', resto_1)