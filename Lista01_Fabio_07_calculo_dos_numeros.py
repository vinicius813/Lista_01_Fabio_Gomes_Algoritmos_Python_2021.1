# Introdução
print('A soma de dois primeiros números mais a diferença entre os dois últimos é: ')
num_1 = float(input('Escreva o primeiro número: '))
num_2 = float(input('Escreva o segundo número: '))
num_3 = float(input('Escreva o terceiro número: '))

# Intermédio
soma = num_1 + num_2
diferenca = num_2 - num_3

# Impressão
print('A soma entre eles será: ', soma)
print('A diferença entre eles será: ', diferenca)
