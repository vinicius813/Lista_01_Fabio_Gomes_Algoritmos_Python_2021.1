# Intro
print('Escreva a divisão pela soma de dois números inteiros: ')
num_1 = float(input('Digite o primeiro número: '))
num_2 = float(input('Digite o segundo número: '))

# Meio da questão
soma = num_1 + num_2
subtracao = num_1 - num_2
divisao = soma / subtracao

# Final
print('A correspondência da divisão entre os números dados é: ', divisao)